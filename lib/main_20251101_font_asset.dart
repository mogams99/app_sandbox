  Widget build(BuildContext context) {
    return Scaffold(
        appBar: AppBar(
          title: const Text('First Screen'),
        ),
        body: Center(
          child: Text(
            'Custom Font Oswald',
            style: TextStyle(
              fontSize: 40,
            ),
          ),
        ));
